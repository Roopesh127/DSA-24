// // Made Calculator in javascript

function Calculator(a,b){
   function Addition(){
    console.log(`Addition ${a+b}`);
   }
   Addition(a,b)

   function Substraction(){
    console.log(`Substraction ${a-b}`)
   }
   Substraction(a,b)

   function Multiplication(){
    console.log(`Multiplication ${a*b}`)
   }
   Multiplication(a,b);

   function Devidation(){
    console.log(`Devidation ${a/b}`);
   }
   Devidation(a,b)
}
Calculator(5,2)
